namespace Emotion.Graphics
{
    /// <summary>
    /// List of data types.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// 4 byte 32 bit int.
        /// </summary>
        Int,
        /// <summary>
        /// 4 byte floating point single precision.
        /// </summary>
        Float,
        /// <summary>
        /// 4 byte 64 bit int.
        /// </summary>
        UnsignedInt,
        /// <summary>
        /// 2 byte unsigned short.
        /// </summary>
        UnsignedShort,
        /// <summary>
        /// 1 byte unsigned.
        /// </summary>
        UnsignedByte
    }
}